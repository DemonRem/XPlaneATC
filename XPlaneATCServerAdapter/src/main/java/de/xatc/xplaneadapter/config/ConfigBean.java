package de.xatc.xplaneadapter.config;

/**
 *
 * @author Mirko Bubel (mirko_bubel@hotmail.com)
 */
public class ConfigBean {
    
    
    private String atcServerName;
    private String atcServerPort;
    private String xplaneListnerIP;
    private String xplaneListenerPort;
    private String connectionName;
    private String atcVoiceServerName;
    private String atcVoiceServerPort;

    public String getAtcServerName() {
        return atcServerName;
    }

    public void setAtcServerName(String atcServerName) {
        this.atcServerName = atcServerName;
    }

    public String getAtcServerPort() {
        return atcServerPort;
    }

    public void setAtcServerPort(String atcServerPort) {
        this.atcServerPort = atcServerPort;
    }

    public String getXplaneListnerIP() {
        return xplaneListnerIP;
    }

    public void setXplaneListnerIP(String xplaneListnerIP) {
        this.xplaneListnerIP = xplaneListnerIP;
    }

    public String getXplaneListenerPort() {
        return xplaneListenerPort;
    }

    public void setXplaneListenerPort(String xplaneListenerPort) {
        this.xplaneListenerPort = xplaneListenerPort;
    }

    public String getConnectionName() {
        return connectionName;
    }

    public void setConnectionName(String connectionName) {
        this.connectionName = connectionName;
    }

    public String getAtcVoiceServerName() {
        return atcVoiceServerName;
    }

    public void setAtcVoiceServerName(String atcVoiceServerName) {
        this.atcVoiceServerName = atcVoiceServerName;
    }

    public String getAtcVoiceServerPort() {
        return atcVoiceServerPort;
    }

    public void setAtcVoiceServerPort(String atcVoiceServerPort) {
        this.atcVoiceServerPort = atcVoiceServerPort;
    }
    
    
    
    
    
    
}
