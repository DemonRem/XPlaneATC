    private DefaultListModel fillPlayerFiles() {

        DefaultListModel model = new DefaultListModel();
        
        List<File> fileList = SwingTools.getFilesAsList("recordings");
        
        for (File f : fileList) {
            
            model.add(0, f.getName());
        }
        
        
        return model;
        
        
    }

