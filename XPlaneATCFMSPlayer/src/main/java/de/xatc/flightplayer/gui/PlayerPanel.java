    private void doItButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doItButtonActionPerformed
        
        if (this.playerFilesList.getSelectedIndices().length < 1) {
            SwingTools.alertWindow("No Files Selected", PlayerConfig.getMainFrame());
        }
        int interval =  0;
        if (StringUtils.isEmpty(this.intervalField.getText())) {
            interval = 5;
        }
        else if (this.intervalField.getText().equals("(seconds)")) {
            interval = 5;
        }
        else {
            interval = Integer.parseInt(this.intervalField.getText());
        }
        
        
        for (String s : this.playerFilesList.getSelectedValuesList()) {
            
            
            
            PlayerControlThread t = new PlayerControlThread(PlayerConfig.getRecordingDirectory() + File.separator + s,interval);
            t.start();
            
            
            
        }
        
        
    }//GEN-LAST:event_doItButtonActionPerformed

